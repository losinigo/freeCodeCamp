// In JavaScript, you can store a value in a variable with the assignment operatior (=)

var myVariable;

// This assigns the number value 5 to myVariable
myVariable = 5;

// If there are any calculations to the right of the operator (=), those are performed 
// before the value is assigned to the variable on the left of the operator.

// First, this code creates a variable named myVar. 
var myVar;
// Then, the code assigns 5 to myVar.
myVar = 5;
// Now, if myVar appears again in the code, the program will treat it as if it is 5.

